#include "pongping_headers/pongping_resource_check_headers.h"






int check_resources_exist(void)
{
   BITMAP_LIST *bL;
   
   
   bL = get_bitmaps("game_menu_list.txt", "game_data");

   if(bL == NULL)
   {
      return PONGPING_RESOURCES_MISSING;
   }
   else
   {
      destroy_bitmap_list(bL);
   }


   return 0;

}



